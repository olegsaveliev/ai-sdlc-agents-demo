#!/usr/bin/env python3
"""
Bye People Application
Simple application that displays "Bye People" farewell message to users.
Related to GitHub Issue #73
"""

def display_bye_people():
    """
    Display the Bye People farewell message.

    Returns:
        str: The Bye People message
    """
    message = "Bye People"
    print(message)
    return message

if __name__ == "__main__":
    display_bye_people()
